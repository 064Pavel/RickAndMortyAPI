DROP TABLE IF EXISTS episode_character;
DROP TABLE IF EXISTS character;
DROP TABLE IF EXISTS location;
DROP TABLE IF EXISTS episode;

create table location
(
    id        serial
        primary key,
    name      text      not null,
    type      text      not null,
    dimension text      not null,
    created   timestamp not null
);
create table episode
(
    id       serial
        primary key,
    name     text      not null,
    air_date text      not null,
    episode  text      not null,
    views    int       not null,
    created  timestamp not null
);
create table character
(
    id          serial primary key,
    name        text not null,
    status      text not null,
    species     text not null,
    type        text not null,
    gender      text not null,
    origin_id   integer,
    location_id integer,
    image       text not null,
    created     timestamp not null,
    constraint fk_character_origin_id
        foreign key (origin_id)
            references location (id)
            on delete cascade,
    constraint fk_character_location_id
        foreign key (location_id)
            references location (id)
            on delete cascade
);

create table episode_character
(
    id           serial primary key,
    episode_id   integer not null,
    character_id integer not null,
    constraint fk_character_episode_episode_id
        foreign key (episode_id)
            references episode (id)
            on delete cascade,
    constraint fk_character_episode_character_id
        foreign key (character_id)
            references character (id)
            on delete cascade
);

create unique index character_episode_episode_id_character_id
    on episode_character (episode_id, character_id);




